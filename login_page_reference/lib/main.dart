import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import 'package:login_page_reference/loginpage.dart';

void main() {
  SystemChrome.setPreferredOrientations([DeviceOrientation.portraitUp]).then((_) {
    runApp(MyApp());
  });
}

